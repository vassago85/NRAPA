## Accreditation

NRAPA is accredited by the South African Police Service (SAPS) with the
designated powers to allocate Dedicated Sport and Dedicated Hunter status to
its members under the Firearms Control Act.

- FAR Sport Shooting: 1300122
- FAR Hunting: 1300127

SAPS makes every licence decision. NRAPA does not issue licences and does not
guarantee any SAPS outcome.

## What NRAPA does

- Manages association membership administration and digital records.
- Allocates Dedicated Sport Shooter and Dedicated Hunter status to qualifying
  members.
- Records member shooting activities and participation for compliance.
- Issues membership certificates and endorsement letters with QR-verifiable
  references.
- Supports lawful firearm owners with information, processes, and
  documentation aligned with SAPS accreditation requirements.

## Membership categories

- **Ordinary membership** — 18 years and older.
- **Dedicated membership** — must complete the dedicated course, maintain
  status, and participate in a minimum of two qualifying activities per year.
  NRAPA is required to inform SAPS of non-compliance.
- **Senior membership** — 61 years and older.
- **Junior membership** — under 18 years.

