# Firearm Licence Motivations

> Firearm licence motivations are written submissions supporting a SAPS
> firearm licence application. Within the Ranyati Group, motivations are
> handled by the **Ranyati Motivations** division — not by NRAPA directly.
> NRAPA's role is association membership, dedicated status, activity records,
> and endorsements.

- Motivations service: https://motivations.ranyati.co.za
- NRAPA endorsements (information page): {{ route('info.endorsements') }}
- NRAPA firearm licence process overview: {{ route('info.firearm-licence-process') }}
- NRAPA site: {{ url('/') }}

## Who does what within the group

- **Ranyati Motivations** drafts professional firearm licence motivation
  letters and supporting documents for SAPS applications.
- **NRAPA** issues membership certificates, dedicated status certificates,
  and endorsement letters for members where appropriate, and maintains the
  association activity records SAPS-accredited associations are expected to
  keep.

If you are looking for a written motivation for a SAPS application, go to
Ranyati Motivations: https://motivations.ranyati.co.za

If you are looking for NRAPA membership or dedicated status, register here:
{{ route('register') }}

## Endorsement letters (NRAPA)

NRAPA may issue endorsement letters to its members in connection with their
association membership and dedicated status (for example, confirming status
or supporting Section 16 type contexts). Endorsements are issued to members
through the member portal after the relevant requirements are met.

See: {{ route('info.endorsements') }}

## Important guardrails

- NRAPA does not draft licence motivations as a service offering on this
  site; that service is provided by Ranyati Motivations.
- No association, including NRAPA, can guarantee a SAPS firearm licence
  outcome. SAPS decides every application based on its own criteria and the
  facts of each case.
- For legal questions about a specific licence application, consult a
  qualified firearm law attorney.

