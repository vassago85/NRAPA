## What NRAPA is

NRAPA is accredited by the South African Police Service (SAPS) with the
designated powers to allocate Dedicated Sport and Dedicated Hunter status to
its members under the Firearms Control Act. NRAPA does not issue firearm
licences — SAPS does. NRAPA supports lawful firearm owners by providing
accredited membership, activity records, endorsements, and compliance tooling
to make Firearms Control Act administration simpler.

