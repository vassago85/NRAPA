## Dedicated sport shooter status

Dedicated sport shooter status is for competitors and active participants who
need association-backed evidence of ongoing sport shooting activity and
organised participation records.

NRAPA provides:

- Membership administration and digital records.
- A dedicated status process aligned with SAPS accreditation.
- Activity logging, scorecards, and certificate generation.
- QR-verifiable certificates for third-party verification.

## Dedicated hunter status

Dedicated hunter status is for active hunters who require association-backed
records of ongoing hunting activity. NRAPA provides the same membership,
records, activity logging, and certification framework for the hunting
pathway.

## What dedicated status is (and is not)

- Dedicated status is an **association status** maintained against
  participation criteria. It is one of the inputs SAPS may consider when
  evaluating certain firearm licence types under the Firearms Control Act.
- Dedicated status is **not a firearm licence** and does not grant any right
  to possess a firearm by itself.
- SAPS decides every firearm licence application. NRAPA does not and cannot
  guarantee any SAPS outcome.

## Maintaining status

Dedicated members must:

- Complete the dedicated course.
- Participate in a minimum of two qualifying shooting/hunting activities per
  year (subject to the criteria of the relevant discipline).
- Keep activity records up to date in the NRAPA member portal.

NRAPA is required to inform SAPS of non-compliance with dedicated status
requirements.

