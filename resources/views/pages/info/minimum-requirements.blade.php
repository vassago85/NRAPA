    <h2>General Requirements</h2>
    <ol>
        <li>Applicant must not be unfit to possess a firearm in terms of the Firearms Control Act (60 of 2000)</li>
        <li>Applicant must be 18 years of age (except Junior membership)</li>
        <li>Complete the membership application form</li>
        <li>Submit a signed copy to the office</li>
        <li>Membership is for 12 months from date of enrolment</li>
        <li>Once-off entry fee plus annual subscription required at enrolment</li>
        <li>Annual subscription payable in month of first enrolment</li>
    </ol>

    <h2>Ordinary Membership</h2>
    <p>Any natural persons between 18 and 60 years of age may apply, accompanied by proof of payment. Membership may be refused if disciplinary measures have been instituted by other accredited associations.</p>

    <h2>Dedicated Membership</h2>
    <ul>
        <li>Must be a fully paid-up member in good standing</li>
        <li>No disciplinary actions pending</li>
        <li>Must indicate written adherence to Association's Code of Conduct at enrolment for the dedicated course</li>
        <li>In event of special merit, exemption may be granted with evidence of high-level provincial, national or international participation</li>
    </ul>

    <h2>Senior Membership</h2>
    <p>Any natural persons from age 61, accompanied by proof of payment. May be refused if disciplinary measures exist.</p>

    <h2>Junior Membership</h2>
    <p>Any person under 18 years, accompanied by proof of payment.</p>
